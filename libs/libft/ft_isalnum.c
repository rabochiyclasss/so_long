#include "libft.h"

int	ft_isalnum(int sign)
{
	if ((sign >= 'A' && sign <= 'Z') || (sign >= 'a' && sign <= 'z')
		|| (sign >= '0' && sign <= '9'))
	{
		return (1);
	}
	return (0);
}
